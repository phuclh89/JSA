CREATE TABLE SYS_SITE (
    SITE_ID          NUMBER(19) NOT NULL,
    SITE_CODE        VARCHAR2(50 CHAR) NOT NULL,
    SITE_NAME        VARCHAR2(200 CHAR) NOT NULL,
    SEQUENCE_CODE    VARCHAR2(50 CHAR) NOT NULL,
    TIMEZONE_NAME    VARCHAR2(100 CHAR) NOT NULL,
    IS_ACTIVE        CHAR(1 CHAR) DEFAULT 'Y' NOT NULL,
    CREATED_AT       TIMESTAMP(6) DEFAULT SYSTIMESTAMP NOT NULL,
    CREATED_BY       VARCHAR2(255 CHAR) NOT NULL,
    UPDATED_AT       TIMESTAMP(6) DEFAULT SYSTIMESTAMP NOT NULL,
    UPDATED_BY       VARCHAR2(255 CHAR) NOT NULL,
    ROW_VERSION      NUMBER(19) DEFAULT 1 NOT NULL,
    CONSTRAINT PK_SYS_SITE PRIMARY KEY (SITE_ID),
    CONSTRAINT UK_SYS_SITE_CODE UNIQUE (SITE_CODE),
    CONSTRAINT UK_SYS_SITE_SEQ_CODE UNIQUE (SEQUENCE_CODE),
    CONSTRAINT CHK_SYS_SITE_ACTIVE CHECK (IS_ACTIVE IN ('Y', 'N')),
    CONSTRAINT CHK_SYS_SITE_ROW_VER CHECK (ROW_VERSION >= 1)
);

