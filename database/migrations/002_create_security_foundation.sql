    CASE WHEN IS_ACTIVE = 'Y' THEN USER_ID END,
    CASE WHEN IS_ACTIVE = 'Y' THEN SCOPE_TYPE END,
    CASE WHEN IS_ACTIVE = 'Y' THEN SITE_ID END,
    CASE WHEN IS_ACTIVE = 'Y' THEN NVL(RIG_ID, -1) END,
    CASE WHEN IS_ACTIVE = 'Y' THEN NVL(DEPARTMENT_ID, -1) END
);
