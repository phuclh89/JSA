CREATE TABLE SYS_USER_DATA_SCOPE (
    USER_DATA_SCOPE_ID NUMBER(19) NOT NULL,
    USER_ID          NUMBER(19) NOT NULL,
    SCOPE_TYPE       VARCHAR2(20 CHAR) NOT NULL,
    SITE_ID          NUMBER(19) NOT NULL,
    RIG_ID           NUMBER(19),
    DEPARTMENT_ID    NUMBER(19),
    CAN_VIEW         CHAR(1 CHAR) DEFAULT 'Y' NOT NULL,
    CAN_ACT          CHAR(1 CHAR) DEFAULT 'N' NOT NULL,
    IS_ACTIVE        CHAR(1 CHAR) DEFAULT 'Y' NOT NULL,
    EFFECTIVE_FROM   TIMESTAMP(6) DEFAULT SYSTIMESTAMP NOT NULL,
    EFFECTIVE_TO     TIMESTAMP(6),
    CREATED_AT       TIMESTAMP(6) DEFAULT SYSTIMESTAMP NOT NULL,
    CREATED_BY       VARCHAR2(255 CHAR) NOT NULL,
    UPDATED_AT       TIMESTAMP(6) DEFAULT SYSTIMESTAMP NOT NULL,
    UPDATED_BY       VARCHAR2(255 CHAR) NOT NULL,
    ROW_VERSION      NUMBER(19) DEFAULT 1 NOT NULL,
    CONSTRAINT PK_SYS_USER_DATA_SCOPE PRIMARY KEY (USER_DATA_SCOPE_ID),
    CONSTRAINT FK_SYS_USER_SCOPE_USER FOREIGN KEY (USER_ID) REFERENCES SYS_USER (USER_ID),
    CONSTRAINT FK_SYS_USER_SCOPE_SITE FOREIGN KEY (SITE_ID) REFERENCES SYS_SITE (SITE_ID),
    CONSTRAINT FK_SYS_USER_SCOPE_RIG FOREIGN KEY (RIG_ID, SITE_ID) REFERENCES SYS_RIG (RIG_ID, SITE_ID),
    CONSTRAINT FK_SYS_USER_SCOPE_DEPT FOREIGN KEY (DEPARTMENT_ID, SITE_ID) REFERENCES SYS_DEPARTMENT (DEPARTMENT_ID, SITE_ID),
    CONSTRAINT FK_SYS_USER_SCOPE_DEPT_RIG FOREIGN KEY (DEPARTMENT_ID, RIG_ID) REFERENCES SYS_DEPARTMENT (DEPARTMENT_ID, RIG_ID),
    CONSTRAINT CHK_SYS_USER_SCOPE_TYPE CHECK (SCOPE_TYPE IN ('SITE', 'RIG', 'DEPARTMENT')),
    CONSTRAINT CHK_SYS_USER_SCOPE_TARGET CHECK (
        (SCOPE_TYPE = 'SITE' AND RIG_ID IS NULL AND DEPARTMENT_ID IS NULL) OR
        (SCOPE_TYPE = 'RIG' AND RIG_ID IS NOT NULL AND DEPARTMENT_ID IS NULL) OR
        (SCOPE_TYPE = 'DEPARTMENT' AND DEPARTMENT_ID IS NOT NULL)
    ),
    CONSTRAINT CHK_SYS_USER_SCOPE_ACCESS CHECK (CAN_VIEW IN ('Y', 'N') AND CAN_ACT IN ('Y', 'N') AND (CAN_ACT = 'N' OR CAN_VIEW = 'Y')),
    CONSTRAINT CHK_SYS_USER_SCOPE_DATES CHECK (EFFECTIVE_TO IS NULL OR EFFECTIVE_TO >= EFFECTIVE_FROM),
    CONSTRAINT CHK_SYS_USER_SCOPE_ACTIVE CHECK (IS_ACTIVE IN ('Y', 'N')),
    CONSTRAINT CHK_SYS_USER_SCOPE_ROW_VER CHECK (ROW_VERSION >= 1)
);

